    Byte numeroByte = 127; // os seus valores encontram-se entre -128 ate 127
    Short numeroShort = -32768; // variam entre -32768 ate 32767
    Integer numeroInteiro = 2147483647; //Variam entre -2147483648 ate 2147483647
